    public static function getParentsTree($category, $title){

        if($category->parent_id != '0'){

            $parent = Category::find($category->parent_id);
            $title = $parent->name . ' > ' . $title;
            return CategoryController::getParentsTree($parent, $title);
        }
        return $title;
    }

